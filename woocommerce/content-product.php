if ( ! defined( 'ABSPATH' ) ) exit; // Exit if accessed directly

global $product, $woocommerce_loop;

// Store loop count we're currently on
if ( empty( $woocommerce_loop['loop'] ) )
	$woocommerce_loop['loop'] = 0;

// Store column count for displaying the grid
if ( empty( $woocommerce_loop['columns'] ) )
	$woocommerce_loop['columns'] = ( is_search() ) ? 4 : 3;

// Ensure visibility
if ( ! $product || ! $product->is_visible() )
	return;

// Increase loop count
$woocommerce_loop['loop']++;

// Extra post classes
$classes = array();
if ( 0 == ( $woocommerce_loop['loop'] - 1 ) % $woocommerce_loop['columns'] )
	$classes[] = 'first';
if ( 0 == $woocommerce_loop['loop'] % $woocommerce_loop['columns'] )
	$classes[] = 'last';

// delimiter
if ( in_array('first', $classes) && $woocommerce_loop['loop'] > 1 )
	echo '<li class="delimiter"></li>';
	
// product item
echo '<li ';
	echo 'data-postid=' . $post->ID . ' ';
	post_class( $classes );
	echo '>';

	get_template_part('views/woocommerce/product-item');
	
echo '</li>';